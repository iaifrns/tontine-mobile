import {
  DarkTheme,
  DefaultTheme,
  ThemeProvider,
} from "@react-navigation/native";
import { useFonts } from "expo-font";
import { router, Stack } from "expo-router";
import * as SplashScreen from "expo-splash-screen";
import { StatusBar } from "expo-status-bar";
import { useEffect } from "react";
import "react-native-reanimated";
import "../global.css";

import ResponseStatus from "@/enum/ResponseStatus";
import useCheckIfIsUserFirstTime from "@/hooks/useCheckIfIsUserFirstTime";
import { useColorScheme } from "@/hooks/useColorScheme";

// Prevent the splash screen from auto-hiding before asset loading is complete.
SplashScreen.preventAutoHideAsync();

export default function RootLayout() {
  const colorScheme = useColorScheme();
  const [loaded] = useFonts({
    SpaceMono: require("../assets/fonts/SpaceMono-Regular.ttf"),
  });
  const response = useCheckIfIsUserFirstTime();

  useEffect(() => {
    if (response.status != ResponseStatus.LOADING && loaded) {
      SplashScreen.hideAsync();
    }
  }, [response, loaded]);

  if (!loaded && response.status == ResponseStatus.LOADING) {
    return null;
  }

  if (response.status == ResponseStatus.SUCCESS && loaded) {
    router.replace("/(onboarding)/first-page");
  }

  return (
    <ThemeProvider value={colorScheme === "dark" ? DarkTheme : DefaultTheme}>
      <Stack>
        <Stack.Screen name="(tabs)" options={{ headerShown: false }} />
        <Stack.Screen name="(onboarding)" options={{ headerShown: false }} />
        <Stack.Screen name="(auth)" options={{ headerShown: false }} />
        <Stack.Screen name="+not-found" />
      </Stack>
      <StatusBar style="auto" />
    </ThemeProvider>
  );
}
